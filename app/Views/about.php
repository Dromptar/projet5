                        <h2>La Metoda HIIT <br/> (Hight Intensity Interval Training)</h2>
                        <p>HIIT est l’acronyme de High Intensity Interval Training en anglais qui veut dire Entraînement fractionné de haute intensité. Le terme HIIT désigne tous les entraînements qui combinent de courtes périodes d’efforts intensifs et explosifs avec de courtes périodes de repos répétées plusieurs fois. Une période d’effort est composée généralement d’un seul exercice et les périodes de repos servent de récupération ou de transition entre les exercices, ces dernières pouvant être des périodes d’inactivité ou de travail modéré. Exemple : sprinter pendant 30 secondes (effort) et marcher pendant 1 minute (repos) – ce cycle est répété 5 fois Protocoles S’il n’existe pas de formule de HIIT unique sur les périodes de travail et de repos, il existe en revanche plusieurs protocoles codifiés comme, par exemple, la méthode Tabata. La méthode Tabata est un entraînement de seulement 4 minutes composé de 8 cycles de 20 secondes d’effort intense et 10 secondes de repos. La méthode Little est un entraînement de 18 ou 27 minutes composé de 8 ou 12 cycles de 60 secondes d’effort soutenu et 75 secondes de repos. Le protocole 8/12 est un entraînement de 20 minutes composé de 60 cycles de 8 secondes de sprint et 12 secondes de repos. Les temps d’effort étant à réaliser à l’intensité maximale dont est capable le pratiquant, ils sont forcément courts. Ils varient aussi suivant le niveau de condition physique de chacun. Durée d’une séance Un entraînement de HIIT type dure en général moins de 30 minutes et implique souvent un rapport 2:1 ou 3:1 entre les périodes de travail et les périodes de récupération. Personnellement, je dépasse rarement 30 secondes de travail car je trouve difficile de maintenir un effort constant à intensité élevée pendant des périodes plus longues tout en maintenant une technique correcte pour chaque répétition d’un exercice. Bénéfices du HIIT Les entraînements de HIIT apportent les mêmes bénéfices sur la santé que les entraînements d’endurance (dits de cardio) mais dans une période beaucoup plus courte. Une étude scientifique de 2014 a comparé pendant 12 semaines, sur des personnes sédentaires, les effets de longues sessions d’endurance à ceux de courtes sessions de HIIT - source : https://www.litobox.com/hiit .</p>
